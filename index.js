// =============================
// GUARDADO AUTOMÁTICO (GLOBAL)
// =============================
function guardarProgresoFormulario() {
  const contenedor = document.querySelector('.active-view');
  if (!contenedor) return;

  const inputs = contenedor.querySelectorAll('input, textarea, select');
  const data = {};

  inputs.forEach(el => {
    const key = el.name || el.id;
    if (!key) return;

    if (el.type === 'checkbox') {
      data[key] = el.checked;
    } else if (el.type === 'radio') {
      if (el.checked) data[key] = el.value;
    } else {
      data[key] = el.value;
    }
  });

  localStorage.setItem('formulario_draft', JSON.stringify(data));
}

// =============================
// RESTAURACIÓN AUTOMÁTICA
// =============================
function restaurarFormulario() {
  const data = JSON.parse(localStorage.getItem('formulario_draft'));
  if (!data) return;

  Object.entries(data).forEach(([key, value]) => {
    const el = document.querySelector(`[name="${key}"], #${key}`);
    if (!el) return;

    if (el.type === 'checkbox') {
      el.checked = value;
    } else if (el.type === 'radio') {
      if (el.value === value) el.checked = true;
    } else {
      el.value = value;
    }
  });
}

