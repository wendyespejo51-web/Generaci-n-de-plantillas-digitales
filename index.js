const circle = document.querySelector(".progress-ring__circle");
const percentText = document.getElementById("loaderPercent");

const radius = 60;
const circumference = 2 * Math.PI * radius;

if (circle) {
  circle.style.strokeDasharray = `${circumference} ${circumference}`;
  circle.style.strokeDashoffset = circumference;
}

function setLoaderProgress(percent) {
  if (!circle || !percentText) return;

  const offset = circumference - (percent / 100) * circumference;
  circle.style.strokeDashoffset = offset;
  percentText.textContent = `${Math.round(percent)}%`;
}

let loaderInterval = null;

function iniciarLoader() {
  let progreso = 15;
  setLoaderProgress(progreso);

  loaderInterval = setInterval(() => {
    if (progreso < 90) {
      progreso += Math.random() * 6;
      setLoaderProgress(progreso);
    }
  }, 250);
}

function finalizarLoader() {
  if (loaderInterval) {
    clearInterval(loaderInterval);
    loaderInterval = null;
  }
  setLoaderProgress(100);
}

//FunciÃ³n FINALIZAR loader
async function finalizarLoader() {
  setLoaderProgress(100);
  await new Promise(r => setTimeout(r, 300)); // micro-pausa visual
  ocultarLoader();
}

