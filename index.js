//FUNCION DE LIMPIEZA PARA CAMPO NODO FINAL (LIMPIAR DOBLE GUION)
function normalizarCodigoAlfaNumerico(valor) { // Limpia espacios, puntos y guiones en cÃ³digos tipo: CEPS.2039 â†’ CEPS2039
  if (typeof valor !== "string") return "";

  return valor
    .trim()
    .replace(/[.\-\s]+/g, ""); // quita . - espacios
}

function limpiarNodoFinal(valor) {
  if (typeof valor !== "string") return "";

  const limpio = valor.trim();

  // âŒ vacÃ­o
  if (!limpio) return "";

  // âŒ solo guiones o signos
  if (/^[\W_]+$/.test(limpio)) return "";

  return limpio;
}

function soloNumeros(valor) {
  if (typeof valor !== "string") return "";
  return valor.replace(/\D+/g, "");
}

