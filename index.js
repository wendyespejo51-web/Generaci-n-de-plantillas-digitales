function aplicarCurvasAutomaticas(respuestaANSI) {
  Object.entries(reglasCurvaANSI).forEach(([funcion, regla]) => {
    const datos = respuestaANSI[funcion];
    if (!datos) return;

    // ðŸ”¹ Validar que existan los datos base
    const cumple = regla.dependeDe.every(
      campo => datos[campo] !== null && datos[campo] !== ""
    );

    if (!cumple) return;

    // =========================
    // ðŸŸ¦ SELECT (CURVA TD)
    // =========================
    if (regla.selectId) {
      const select = document.getElementById(regla.selectId);
      if (select) {
        select.value = regla.valor;
        select.dataset.autorellenado = "true";
      }
    }

    // =========================
    // ðŸŸ© INPUT TEXTO 1 (Forward)
    // =========================
    if (regla.inputid) {
      const input1 = document.getElementById(regla.inputid);
      if (input1 && !input1.value) {
        input1.value = regla.valortextinputid ?? "";
        input1.dataset.autorellenado = "true";
      }
    }

    // =========================
    // ðŸŸ© INPUT TEXTO 2 (Ãngulo)
    // =========================
    if (regla.inputid2) {
      const input2 = document.getElementById(regla.inputid2);
      if (input2 && !input2.value) {
        input2.value = regla.valortextinputid2 ?? "";
        input2.dataset.autorellenado = "true";
      }
    }
  });
}

