// FUNCIÓN PARA LIMPIAR CAMPOS DEL FORMULARIO ACTUAL
// =============================
function limpiarFormulario() {

  const vistas = document.querySelectorAll(
    "#viewParte1, #viewParte2, #viewParte3, #viewCambioAjuste, #viewPuestaServicio"
  );

  vistas.forEach(vista => {
    let camposExcluidos = [];

    // Determinar exclusiones según la vista
    if (vista.id.startsWith("viewParte")) {
      camposExcluidos = ["CodigoUnico", "fecha", "semana", "hora"];
    }
    else if (vista.id === "viewCambioAjuste") {
      camposExcluidos = ["CodigoUnicoCA", "fechaCA", "SemanaCA", "VnReleCA", "NCatalogo"];
    }
    else if (vista.id === "viewPuestaServicio") {
      camposExcluidos = ["CodigoUnicoPS", "fechaPS", "SemanaPS", "horaPS", "ProtocoloRC"];
    }

    // Recorrer todos los campos de esta vista
    vista.querySelectorAll("input, select, textarea").forEach(el => {

      // No limpiar los excluidos
      if (camposExcluidos.includes(el.id)) return;

      // Limpieza según tipo
      if (el.type === "checkbox" || el.type === "radio") {
        el.checked = false;
      }
      else if (el.tagName.toLowerCase() === "select") {
        el.selectedIndex = 0;
      }
      else {
        el.value = "";
      }
    });
  });
}

