function esperarElementos(ids, intentos = 0) {

  return new Promise((resolve, reject) => {

    const faltantes = ids.filter(id => !document.getElementById(id));

    if (faltantes.length === 0) {
      resolve();
      return;
    }

    if (intentos > 20) {
      reject("Campos no encontrados: " + faltantes.join(", "));
      return;
    }

    setTimeout(() => {
      esperarElementos(ids, intentos + 1)
        .then(resolve)
        .catch(reject);
    }, 300);
  });
}


function normalizarNumero(valor) {
  const match = String(valor).match(/-?\d+(\.\d+)?/);
  return match ? match[0] : "";
}

function aplicarDatos(data) {

  for (const key of Object.keys(data)) {

    const el = document.getElementById(key);
    if (!el) continue;

    let valor = data[key];
    if (valor === null || valor === undefined) continue;

    // Para Choice / Lookup
    if (typeof valor === "object" && valor.Value !== undefined) {
      valor = valor.Value;
    }

    if (el.type === "number") {
      el.value = normalizarNumero(valor);
    }
    else if (el.type === "checkbox") {
      el.checked = Boolean(valor);
    }
    else if (el.tagName === "SELECT") {
      el.value = valor;
      el.dispatchEvent(new Event("change"));
    }
    else {
      el.value = valor;
    }
  }
}

